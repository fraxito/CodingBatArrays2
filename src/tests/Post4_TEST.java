    private int [] miPost4(int[] nums) {
        int estaCuatro = 0;
        int total;
        //Calculara la posicion de el ultimo 4
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] == 4) {
                estaCuatro = i;
            }
        }
        total = nums.length - estaCuatro - 1;
        int array[] = new int [total];
        for (int j = 0; j < total; j++) {
            estaCuatro++;
            array[j] = nums[estaCuatro];
        }
        jLabelGenerado.setText("Resultado esperado: " + Arrays.toString(array));
        return array;
    }
